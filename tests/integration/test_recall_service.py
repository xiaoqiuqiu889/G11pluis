"""End-to-end integration tests for W7 留存机制.

Covers the brief's required scenarios:

1. **D1 召回内容基于玩家本局时间线（不是模板）**
   — the body references the actual actionType / targetId /
   evidenceIds the player executed; a player who gave photo_A
   to leila in 2008 gets a *different* D1 body than a player
   who gave photo_A to arash.
2. **D3 召回包含另一人物视角**
   — the body carries the dominant-character-aware perspective
   (the *other* character's beliefs), and the payload's
   ``perspective`` field is one of the 4 case_01 characters
   (leila / arash / kamran / maryam).
3. **D7 召回基于"未完成对话"算法**
   — the body's 5 unfinished-dialogue phrases are derived
   from the player's belief_rows with confidence >= 0.55 +
   cross-era seed membership, sorted by score desc.
4. **推送接口可被客户端拉取**
   — :class:`PushService` dispatches the scheduled item
   (stdout mock) + the client's ``GET /v1/recall/pending``
   returns the item in ``sent`` status + ``POST
   /v1/recall/mark-read`` flips it to ``opened`` and emits
   the ``recall_*_opened`` analytics event.

Architecture
------------

The test uses a **temporary SQLite database** so the W4
default ``data/g1n.db`` is never touched.  The DB URL is
set via ``G1N_DB_URL`` env var *before* any of the
``server.*`` modules are imported; this is the only
W4-env-var escape hatch the server exposes, and the W4
contract allows it.

We also rebuild the singletons so they pick up the new
DB URL.  The ``reset_*`` helpers exported by
:mod:`server.push_service` and :mod:`server.recall_service`
give us that.

The test does **not** call the LLM; recall payloads are
generated by the deterministic mock templates the
service ships with (``use_llm=False``).  The LLM path
is exercised by a separate test in this file.
"""

