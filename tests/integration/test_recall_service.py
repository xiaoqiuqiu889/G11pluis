def _make_run(*, user_id: str = "demo-user", ended_at: datetime | None = None) -> GameRun:
    """Insert a run row directly (skipping the W4 happy-path which
    also seeds entitlements).  Tests need the *minimum* state.
    """

    run_id = str(uuid.uuid4())
    with SessionLocal() as s:
        run = GameRun(
            run_id=run_id,
            user_id=user_id,
            case_slug="case_01_revolution_street",
            current_scene_id="reunion_2024",
            era="2024",
            event_sequence=0,
            phase="ended",
            is_archived=False,
            is_mock=True,
            ended_at=ended_at or datetime.now(timezone.utc),
        )
        s.add(run)
        s.commit()
        s.refresh(run)
    return run


def _add_event(
    *,
    run_id: str,
    scene_id: str,
    actor_id: str,
    action_type: str,
    payload: dict,
    event_sequence: int = 1,
    idempotency_key: str | None = None,
) -> None:
    with SessionLocal() as s:
        s.add(GameEventRow(
            run_id=run_id,
            event_sequence=event_sequence,
            scene_id=scene_id,
            actor_id=actor_id,
            action_type=action_type,
            action_payload_json=json.dumps(payload, ensure_ascii=False),
            idempotency_key=idempotency_key or str(uuid.uuid4()),
            outcome_id=str(uuid.uuid4()),
        ))
        s.commit()


def _add_belief(
    *,
    run_id: str,
    character_id: str,
    subject: str,
    state: str,
    confidence: float,
    event_sequence: int,
) -> None:
    with SessionLocal() as s:
        s.add(CharacterBeliefRow(
            run_id=run_id,
            character_id=character_id,
            subject=subject,
            belief_state=state,
            confidence=confidence,
            event_sequence=event_sequence,
        ))
        s.commit()


def _add_seed(
    *,
    run_id: str,
    seed_id: str,
    is_dormant: bool = True,
    fired_at_event: int | None = None,
) -> None:
    with SessionLocal() as s:
        s.add(CausalSeedRow(
            run_id=run_id,
            seed_id=seed_id,
            source_scene="photo_lab_2008",
            description=f"seed {seed_id}",
            is_dormant=is_dormant,
            fired_at_event=fired_at_event,
        ))
        s.commit()


def _make_recall_service() -> RecallService:
    """Fresh RecallService singleton (no LLM refinement)."""

    reset_default_recall_service()
    reset_default_push_service()
    return RecallService(use_llm=False)


def _make_push_service(recall: RecallService) -> PushService:
    """PushService with a captured stdout for assertion."""

    reset_default_push_service()
    buf = io.StringIO()
    push = PushService(recall_service=recall, stdout=buf)
    push._stdout_for_test = buf  # type: ignore[attr-defined]
    return push


def _set_last_active_at(run_id: str, when: datetime) -> None:
    """Override game_runs.last_active_at so D1/D3/D7 fire_at math is
    deterministic in the test."""

    with SessionLocal() as s:
        run = s.get(GameRun, run_id)
        if run is not None:
            run.last_active_at = when
            s.commit()


